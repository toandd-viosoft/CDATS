def calc_line_rate(arr, n_ports = 1):
    ret = []
    for a in arr:
        ret.append(float(1250000000*n_ports)/(a+20) / 1000000)
    return ret;

def line_rate_to_pps(pkt_size, n_ports):
    # FIXME Don't hardcode 10Gb/s
    return n_ports * float(10000000000 / 8) / (pkt_size + 20)
